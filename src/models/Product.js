class Product {
  constructor(
    _id,
    title,
    description,
    price,
    thumbnail,
    stock,
    category,
    quantity
  ) {
    this._id = _id;
    this.title = title;
    this.description = description;
    this.price = price;
    this.thumbnail = thumbnail;
    this.stock = stock;
    this.category = category;
    this.quantity = this.quantity;
  }
}

export default Product;
